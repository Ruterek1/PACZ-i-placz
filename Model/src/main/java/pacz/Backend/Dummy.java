package pacz.Backend;

public class Dummy {

    private double distance;
    private double hp;

    public Dummy(double distance, double HP) {
        this.distance = distance;
        this.hp = HP;
    }

    public double getDistance() {
        return distance;
    }

    public void setDistance(double distance) {
        this.distance = distance;
    }

    public double getHp() {
        return hp;
    }

    public void setHp(double hp) {
        this.hp = hp;
    }
}
